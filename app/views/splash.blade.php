									<h3>What is hackGFS?</h3>
									<p>hackGFS is the first hackathon run by students from Germantown Friends School in Philadelphia.  A hackathon is a social event where you are given time to build something dope while meeting new people and expanding your knowledge.  </p>
								</section>
								<section>
									<span class="icon major fa-lock"></span>
									<h3>Who can attend hackGFS?</h3>
									<p>Anyone currently enrolled in high school can apply to attend hackGFS.  Unfortunately, high school graduates cannot participate as a hacker, but you can be a mentor and help others! The only thing that we require from our participants is that they follow this <a href="http://static.mlh.io/docs/mlh-code-of-conduct.pdf" target="_blank">code of conduct</a></p>
								</section>
								<section>
									<span class="icon major fa-cog"></span>
									<h3>How much does it cost?</h3>
									<p>Free!  hackGFS is a free event that will provide you with meals, snacks, classic hackathon swag like shirts and sunglasses.  Unfortunately, we will not be providing travel reimbursement for this event.</p>
								</section>
								<section>
									<span class="icon major fa-desktop"></span>
									<h3>How do teams work?</h3>
									<p>Teams can be no larger than 4 developers, but can be groups of 2, 3, or you can work by yourself!  Upon reviewing applications we will post a link to a facebook group of accepted applicants so you can meet people attending.</p>
								</section>
								<section>
									<span class="icon major fa-chain"></span>
									<h3>What if I don't know how to code?</h3>
									<p>That's fine!  hackGFS is an event for people of all skillsets.  We will have workshops throughout the event so you can learn from experienced developers. </p>
								</section>
								<section>
									<span class="icon major fa-diamond"></span>
									<h3>Can I start my hack before the event?</h3>
									<p>Nope!  Any hack you will be submitting for prizes and review from judges must be started after our kickoff for fairness.</p>
								</section>
